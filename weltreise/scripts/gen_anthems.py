"""Generate synthesized national anthem snippets as WAV files."""
import wave, math, struct, os, array

SR = 22050
OUT = os.path.join(os.path.dirname(__file__), '..', 'assets', 'audio', 'anthems')
os.makedirs(OUT, exist_ok=True)

SEMIS = {'C':0,'D':2,'E':4,'F':5,'G':7,'A':9,'B':11}

def freq(name):
    if name == 'R': return 0.0
    i = 0
    n = SEMIS[name[i]]; i += 1
    if i < len(name) and name[i] in '#b':
        n += 1 if name[i] == '#' else -1; i += 1
    oct = int(name[i])
    midi = 12 * (oct + 1) + n
    return 440.0 * 2 ** ((midi - 69) / 12.0)

def make_wav(country, melody, bpm=90):
    samples = []
    beat = 60.0 / bpm
    total = sum(b for _, b in melody)
    elapsed = 0
    for note, beats in melody:
        ns = int(beats * beat * SR)
        elapsed += beats
        if note == 'R' or ns == 0:
            samples += [0] * ns
            continue
        f = freq(note)
        for i in range(ns):
            t = i / SR
            # Organ-like tone with harmonics
            v = (math.sin(2*math.pi*f*t)   * 0.55 +
                 math.sin(2*math.pi*f*2*t) * 0.22 +
                 math.sin(2*math.pi*f*3*t) * 0.12 +
                 math.sin(2*math.pi*f*4*t) * 0.06 +
                 math.sin(2*math.pi*f*0.5*t)*0.05)
            # Per-note envelope
            att = min(i / max(0.03 * SR, 1), 1.0)
            rel_s = int(ns * 0.78)
            rel = max(0.0, 1.0 - (i - rel_s) / max(ns - rel_s, 1)) if i > rel_s else 1.0
            # Global fade-out in last 1.5 beats
            fade_start = (total - 1.5) * beat * SR
            gl = max(0.0, 1.0 - (len(samples) + i - fade_start) / (1.5 * beat * SR)) if (len(samples) + i) > fade_start else 1.0
            samples.append(int(v * att * rel * gl * 27000))

    data = array.array('h', [max(-32767, min(32767, s)) for s in samples])
    path = os.path.join(OUT, f'{country}.wav')
    with wave.open(path, 'w') as w:
        w.setnchannels(1); w.setsampwidth(2); w.setframerate(SR)
        w.writeframes(data.tobytes())
    print(f'  {country}.wav  ({len(samples)//SR:.1f}s)')

